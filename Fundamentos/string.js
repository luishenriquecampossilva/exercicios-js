console.log(escola.substring(1))//retorna um valor a parti de uma deterninada posicao em uma string
console.log(escola.substring(0,3))//retorna a partir do indice 0 ate o indice 3 sem incluir o indice 3
console.log('escola '.concat(escola).concat(" !"))//concatena elementos
console.log(escola.replace(3,'e'))//Substitui um valor pelo outro
